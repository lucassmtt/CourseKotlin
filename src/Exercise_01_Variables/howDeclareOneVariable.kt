package Exercise_01_Variables

fun main(){
    val firstName : String = "Lucas"  // Does not change the value
    var secondName : String = "Mota" // Change the value
    secondName = "Motta"

    println(firstName)
    println(secondName)

    val otherName = "Gabriel" // Automatic assignment
    val othername : String    //
    othername = "Freitas"

    println(othername)
    println(otherName)


}